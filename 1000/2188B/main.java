import java.util.*;
public class main
{
    public static void main(String args[])
    {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        
        while(t-- > 0)
        {
            int n = sc.nextInt();
            
            String s = sc.next();
            
            int cnt = 0;
            int zero = 1;
            for(int i=0;i<n;i++)
            {
                if(s.charAt(i) == '1')
                {
                    cnt++;
                    cnt += zero/3;
                    zero = 0;
                }
                else
                    zero++;
            }
            
            zero++;
            cnt += zero/3;
            
            System.out.println(cnt);
        }
    }
}