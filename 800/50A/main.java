import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // Read two integers m and n
        int m = sc.nextInt();
        int n = sc.nextInt();

        // Compute (m * n) / 2 and print the result
        System.out.println((m * n) / 2);
    }
}
